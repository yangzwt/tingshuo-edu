public class WhileDemo1 {
    public static void main(String[] args) {
        /**
         * 需求:使用while 循环打印 1-100
         */
         // while :开始是1,结束100
        int i=1;
        while (i<=100){
            System.out.println(i);
            i++;
        }
    }
}
